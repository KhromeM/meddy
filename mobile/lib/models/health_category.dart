class HealthCategory {
  final String name;
  final int score;
  final String oneLineSummary;
  final String goldTest;
  final String result;
  final String range;
  final String recommendation;

  HealthCategory({
    required this.name,
    required this.score,
    required this.oneLineSummary,
    required this.goldTest,
    required this.result,
    required this.range,
    required this.recommendation,
  });
}
